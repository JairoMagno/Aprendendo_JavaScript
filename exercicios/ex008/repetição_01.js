//Aprendendo repetições em JavaScript

var c = 1
var cont = 1


while (c <= 6) {
    console.log(`Passo ${c}`)
    c ++
}

//--------------------------------------------------

do{
    console.log(`Contagem ${cont}`)
    cont ++
} while (cont <= 6)

//--------------------------------------------------

for (contador = 1; contador <= 6; contador++) {
    console.log(`Tentativa ${contador}`)
} 